package Unidad2;

public class EjercicioString {
	
	    public static void main(String[] args) {
	        // Crear una variable de tipo String con un texto
	        String texto = "Estoy aprendiendo Java con ejemplos";

	        // Mostrar la longitud de la cadena
	        System.out.println("Longitud de la cadena: " + texto.length());

	        // Mostrar el texto en mayúsculas
	        System.out.println("Texto en mayúsculas: " + texto.toUpperCase());

	        // Verificar si contiene la palabra "Java"
	        System.out.println("¿Contiene la palabra 'Java'? " + texto.contains("Java"));
	    }
	}


