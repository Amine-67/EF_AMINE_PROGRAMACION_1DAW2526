package Unidad1;

public class EjemploPrintF { 
	
	public static void main(String[] args) {
		
		// Ejemplo de printf con números enteros y saldo de línea.
		System.out.printf("La mitd de %d es %d \n y suman %d. \n",42, 21, 63);
		System.out.print("Fin");
		
		// Ejemplo de printf con números double
		double precio = 5.75;
		System.out.printf("El precio es: %f", precio);
	}
}
